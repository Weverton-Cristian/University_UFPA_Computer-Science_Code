# 1 Fazer um programa que receba as medidas de um paralelepípedo e forneça seu volume

a = float(input('Forneça a altura do paralelepípedo: '))
b = float(input('Forneça a largura do paralelepípedo: '))
c = float(input('Forneça a profundidade do paralelepípedo: '))

volume = a*b*c
print('O volume obtido em cm3 é:',volume)