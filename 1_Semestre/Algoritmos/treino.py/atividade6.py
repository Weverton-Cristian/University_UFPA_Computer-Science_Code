n = 1
cont = float(input('Forneca o valor das repetiçoes '))
while n <= cont:
    v1 = float(input('Forneça o primeiro valor: '))
    v2 = float(input('Forneça o segundo valor: '))
    v3 = float(input('Forneça o terceiro valor: '))
    média = (v1+v2+v3)/3
    print('O valor da média é:',média)
    n += 1
print('fim')